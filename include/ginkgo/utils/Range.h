#ifndef __UTILS__RANGE_H
#define __UTILS__RANGE_H

namespace ginkgo
{

////////////////////////////////////////////////////////////////////////////////////////////////////
//
// Range
//
////////////////////////////////////////////////////////////////////////////////////////////////////

template<class T>
struct Range
{
	T min;
	T max;
};

////////////////////////////////////////////////////////////////////////////////////////////////////

template<class T>
inline bool operator==(const Range<T> &lhs, const Range<T> &rhs)
{
	return lhs.min == rhs.min && lhs.max == rhs.max;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

}

#endif
