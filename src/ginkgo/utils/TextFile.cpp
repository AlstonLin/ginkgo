#include <ginkgo/utils/TextFile.h>

#include <iostream>

namespace ginkgo
{

////////////////////////////////////////////////////////////////////////////////////////////////////
//
// TextFile
//
////////////////////////////////////////////////////////////////////////////////////////////////////

TextFile::TextFile(const boost::filesystem::path &fileName)
:	m_fileStream(fileName.string())
{
	if (!m_fileStream.good())
	{
		std::cerr << "\033[1;31m[Error] Could not read file " << fileName << "\033[0m" << std::endl;
		exit(EXIT_FAILURE);
	}
}

////////////////////////////////////////////////////////////////////////////////////////////////////

std::ifstream &TextFile::read()
{
	return m_fileStream;
}

////////////////////////////////////////////////////////////////////////////////////////////////////

void TextFile::seekBeginning()
{
	m_fileStream.clear();
	m_fileStream.seekg(0, std::ios::beg);
}

////////////////////////////////////////////////////////////////////////////////////////////////////

}
